    private void btnIguallActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIguallActionPerformed

        valor2=Double.parseDouble(txtResultado.getText());
        if (sinal=="soma") {
            txtResultado.setText(String.valueOf(valor1+valor2));
        }
        if (sinal=="subtrair") {
            txtResultado.setText(String.valueOf(valor1-valor2));           
        }
        if (sinal=="multiplicar") {
            txtResultado.setText(String.valueOf(valor1*valor2));            
        }
        if (sinal=="dividir") {
            txtResultado.setText(String.valueOf(valor1/valor2));            
        }
    }//GEN-LAST:event_btnIguallActionPerformed

